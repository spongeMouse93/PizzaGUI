package pizza.pizza;

/**
 * A helper enum used to designate the size of a pizza
 * @author Siddharth Sircar
 * @author Yash Shah
 * @since November 6, 2023
 */
public enum Size {
    /**
     * Used to indicate a small pizza
     */
    SMALL,
    /**
     * Used to indicate a large pizza
     */
    LARGE,
    /**
     * Used to indicate a medium pizza
     */
    MEDIUM;
}
